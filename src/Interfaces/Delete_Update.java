    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       this.buttonEnableThroug();
        // Delete steps 
        BookItm itm =new BookItm();
        itm.setIsbn(txtIsbn.getText());
        Components com=new Components();
        com.deleteBooks(itm);
        // Insert the String to the tree for remove
        Btree.delete(Integer.parseInt(txtIsbn.getText()));
        DisplayBooks();
        clearTxt();
       Btree.traverse();
    }//GEN-LAST:event_btnDeleteActionPerformed

