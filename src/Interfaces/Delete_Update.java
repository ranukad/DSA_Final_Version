    private void tableBooksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableBooksMouseClicked
         try {
            ResultSet rs=null;
            ConnectionClass con= new ConnectionClass();
            Connection c=con.getConnection();
            int row=tableBooks.getSelectedRow();
            String TableClick=(tableBooks.getModel().getValueAt(row, 0).toString());
            String sql="select ISBN,BookCatag,BookName,AuotherFname,AuthorSname from books where ISBN='"+TableClick+"' ";
            PreparedStatement pst=c.prepareStatement(sql);
            
            rs=pst.executeQuery();
            if(rs.next()){
//                String addId=rs.getString("ID");
//                txtId.setText(addId);
                String add=rs.getString("ISBN");
                txtIsbn.setText(add);
                String add1=rs.getString("BookName");
                txtBname.setText(add1);
                String add2=rs.getString("AuotherFname");
                txtAfname.setText(add2);
               String add3=rs.getString("AuthorSname");
               txtAsname.setText(add3);
                
                    
                    
            }
            btnDelete.setEnabled(true);
            
        } catch (Exception ex) {
            Logger.getLogger(Delete_Update.class.getName()).log(Level.SEVERE, null, ex);
        }
             
    }//GEN-LAST:event_tableBooksMouseClicked
